class UnitType:
    """
    This class includes various constants that can be used to represent
    different types of units, including; CP, INF1, INF2, INF3, GEV,
    HVY, WHZ, MSL, and OGRE.
    """
    CP = 'CP'
    INF1 = 'INF1'
    INF2 = 'INF2'
    INF3 = 'INF3'
    GEV = 'GEV'
    HVY = 'HVY'
    HWZ = 'HWZ'
    MSL = 'MSL'
    OGRE = 'OGRE'
